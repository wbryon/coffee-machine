package com.hyperskill;

class Fill {

    private int water = 0;
    private int milk = 0;
    private int coffee = 0;
    private int cupsAmount = 0;

    public int getWater() {
        return this.water;
    }

    public void setWater(int water) {
        this.water = water;
    }

    public int getMilk() { return this.milk; }

    public void setMilk(int milk) {
        this.milk = milk;
    }

    public int getCoffee() { return this.coffee; }

    public void setCoffee(int coffee) {
        this.coffee = coffee;
    }

    public int getCupsAmount() { return cupsAmount; }

    public void setCupsAmount(int cupsAmount) {
        this.cupsAmount = cupsAmount;
    }
}